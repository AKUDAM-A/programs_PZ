# Программа вычисляет незанятую часть отрезка A длины после размещения отрезков длины B

while True:
    try:
        A = int(input("Введите значение A (целое положительное число): "))
        if A <= 0:
            print("Число должно быть положительным.")
            continue
        break
    except:
        print("Ошибка! Введите целое число.")

while True:
    try:
        B = int(input("Введите значение B (целое положительное число, меньше A): "))
        if B <= 0 or B >= A:
            print("Число должно быть положительным и меньше A.")
            continue
        break
    except:
        print("Ошибка! Введите целое число.")

result = A % B

print("Длина незанятой части отрезка:", result)
print("Программа успешно завершена!")
