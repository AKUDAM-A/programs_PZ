matrix = [
    [4, -2, 7, 1],
    [9, 3, -5, 8],
    [6, 0, 2, -4],
    [5, 11, -1, 3]
]

print("Исходная матрица:")
for row in matrix:
    print(row)

# Одномерный массив соответствующей длины
new_row = [10, 20, 30, 40]

# Замена третьей строки (индекс 2)
matrix[2] = new_row[:]

print("\nМатрица после замены третьей строки:")
for row in matrix:
    print(row)
