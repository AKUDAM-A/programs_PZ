# Ввод строки
path = input("Введите полное имя файла: ")

print("Исходная строка:", path)

# Разделение по символу '\'
parts = path.split("\\")

# Если каталогов нет (например: C:\file.txt)
if len(parts) <= 2:
    print("Последний каталог: !")
else:
    # Последний каталог — предпоследний элемент
    print("Последний каталог:", parts[-2])
